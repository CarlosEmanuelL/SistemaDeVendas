    private void listarValores() {

        try {
           VendaTotalDAO objvendatotaldao = new VendaTotalDAO();
            DefaultTableModel model = (DefaultTableModel) tabelaHistoricoVendas.getModel();
            model.setNumRows(0);

            ArrayList<VendaTotalDTO> lista = objvendatotaldao.ListarVendaTotal();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getId_venda(),
                    lista.get(num).getCliente_venda(),
                    lista.get(num).getForma_pagamento_venda(),
                    lista.get(num).getDesconto_venda(),
                    lista.get(num).getValor_pago_venda(),
                    lista.get(num).getTotal_produto_venda(),
                    lista.get(num).getTotal_venda(),
                    lista.get(num).getTroco_venda()
                    
                });
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Listar Valores HisotricoVendasPainel: " + erro);
        }
    }
    
